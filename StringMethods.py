#Metodos String
mayusculas = "Esta funcion pone las letras mayusculas"
print(mayusculas.upper())# Function UPPER

minusculas = "ESTA FUNCION PONE LAS LETRAS MINUSCULAS"
print(minusculas.lower()) # Function LOWER

removeSpaces = "   Hey mis espacios son eliminados al principio y al final    "
print(removeSpaces.strip()) #Function Strip quita los espacios

replaceString = "El texto sera reemplazado por: "
print(replaceString.replace("Por este TEXTO")) #Function Replace

separador = "Separar el texto con split , si tu quiere"
print(separador.split("/"))

